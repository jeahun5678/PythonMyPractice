# 입력을 받습니다.

number1 = input("첫번째 숫자> ")
number2 = input("두번째 숫자> ")

number1 = float(number1)
number2 = float(number2)


# 출력합니다.
print("더하기 값: ", number1 + number2)
print("빼기 값: ", number1 - number2)
print("곱하기 값: ", number1 * number2)
print("나누기 값: ", number1 / number2, "나머지: ", number1 % number2)
print()
print("모든 계산을 완료하였습니다.")